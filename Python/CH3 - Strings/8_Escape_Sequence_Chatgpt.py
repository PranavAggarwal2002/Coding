print("Demonstration of Python Escape Sequences\n")

# New line
print("\\n -> Creates a new line:")
print("Hello\nWorld")

# Tab space
print("\n\\t -> Adds a horizontal tab space:")
print("Hello\tWorld")

# Backslash
print("\n\\\\ -> Prints a single backslash:")
print("This is a backslash: \\")

# Single quote
print("\n\\' -> Allows single quote inside string:")
print('It\'s a sunny day')

# Double quote
print("\n\\\" -> Allows double quote inside string:")
print("He said \"Python is easy\"")

# Carriage return
print("\n\\r -> Moves cursor to start of line (overwrites text):")
print("Hello\rWorld")

# Backspace
print("\n\\b -> Removes one character before it:")
print("Helloo\b")

# Form feed
print("\n\\f -> Form feed (page break, rarely used):")
print("Hello\fWorld")

# Vertical tab
print("\n\\v -> Vertical tab (adds vertical spacing):")
print("Hello\vWorld")