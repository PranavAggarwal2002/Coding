class Relationaloperatorsgreator {
    public static void main(String args[]) {
        int x = 7;
        System.out.println("num 1 is " + x);
        int y = 5;
        System.out.println("num 2 is " + y);
        boolean result = x > y;
        System.out.println("x > y");
        System.out.println(result);
    }
}

